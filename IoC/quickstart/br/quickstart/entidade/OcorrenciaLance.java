package br.quickstart.entidade;

public class OcorrenciaLance {
	private String jogo,listJogador,tempo,listLance;

	public String getJogo() {
		return jogo;
	}

	public void setJogo(String jogo) {
		this.jogo = jogo;
	}

	public String getListJogador() {
		return listJogador;
	}

	public void setListJogador(String jogador) {
		this.listJogador = jogador;
	}

	public String getTempo() {
		return tempo;
	}

	public void setTempo(String tempo) {
		this.tempo = tempo;
	}

	public String getListLance() {
		return listLance;
	}

	public void setListLance(String lance) {
		this.listLance = lance;
	}
}
